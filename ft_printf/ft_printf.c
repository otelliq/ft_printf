#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	va_list	args;
	int		i;
	int		length;

	i = 0;
	length = 0;
	va_start(args, format);
	while (format[i])
	{
		if (format[i] == '%')
		{
			length += ft_formatchooser(format[++i], args);
			i++;
		}
		else
			length += ft_putcharr(format[i++]);
	}
	va_end(args);
	return (length);
}
